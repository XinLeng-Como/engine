/**
 * Mouse buttons.
 *
 * @readonly
 * @enum {ValueOf<typeof MouseButton>}
 * @category Input
 */
export const MouseButton = Object.freeze({
    /** No specific projection format is used. */
    None: -1,

    /** The left mouse button. */
    Left: 0,

    /** The middle mouse button. */
    Middle: 1,

    /** The right mouse button. */
    Right: 2
});
